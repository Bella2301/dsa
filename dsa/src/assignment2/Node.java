package assignment2;

/**
 * The Node class represents a node in a linked list.
 * Each node contains an integer data value and a reference
 * to the next node in the list.
 */

class Node {
    int data; // data value stored in the node
    Node next; // referencing to the next node

    

    /**
     * Constructs a new node with the specified data value.
     * 
     * @param data The integer data value to be stored in the node.
     */
    
    Node(int data) {
        this.data = data;
        this.next = null;
    }
}






 
